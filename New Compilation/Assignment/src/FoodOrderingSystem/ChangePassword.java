    private boolean changePassword(String Password){
        File inputFile = new File("User.txt");
        File tempFile = new File("Temp_User.txt");
        boolean isUpdated = false;
        
        try(BufferedReader br = new BufferedReader(new FileReader(inputFile));
            BufferedWriter bw = new BufferedWriter(new FileWriter(tempFile))){
            
            String line;
            
            while((line = br.readLine()) != null){
                String[] data = line.split(";");
                
                if(data.length >= 7 && data[0].equalsIgnoreCase(this.userId)){
                    data[3] = String.valueOf(Password);
                    isUpdated = true;
                }
                bw.write(String.join(";", data));
                bw.newLine();
            }
        }catch(IOException e){
            
        }
        
        if(isUpdated){
            if(!inputFile.delete() || !tempFile.renameTo(inputFile)){
                System.out.println("Error update password");
                return false;
            }
        }else{
            tempFile.delete();
        }
        return isUpdated;
    }
    
