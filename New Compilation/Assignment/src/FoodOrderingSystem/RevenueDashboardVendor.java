    private void CalculateTotalOrders(){
        String vendorID = this.vendorId;
        int totalOrders = 0;
        double totalAmount = 0.0;
        
        try(BufferedReader br = new BufferedReader(new FileReader("Order Status.txt"))){
            String line;
            
            while((line = br.readLine()) != null){
                String[] data = line.split(";");
                
                if(data.length >= 12){
                    String fileVendorID = data[1].trim();
                    String amountStr = data[8].trim();
                    String status = data[11].trim();
                    
                    if(fileVendorID.equalsIgnoreCase(vendorID) && (status.equalsIgnoreCase("Completed") || status.equalsIgnoreCase("Cancelled"))){
                        totalOrders++;
                        
                        try{
                            double amount = Double.parseDouble(amountStr);
                            totalAmount += amount;
                        }catch(NumberFormatException e){
                            System.out.println("Invalid amount format: " + amountStr);
                        }
                        
                    }
                    
                }else{
                    System.out.println("Skipping malformed line: " + line);
                }
                
            }
            
            AmountLbl.setText("RM " + String.format("%.2f", totalAmount));
            TotalOrderLbl.setText("Orders: " + totalOrders);
            
        }catch(IOException e){
            JOptionPane.showMessageDialog(this, "Error loading order file: " + e.getMessage(), "File Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
    private void loadTransaction(){
        DefaultTableModel model = (DefaultTableModel) RevenueTable.getModel();
        model.setRowCount(0);
        
        try(BufferedReader br = new BufferedReader(new FileReader("Transaction.txt"))){
            String line;
            
            while((line = br.readLine()) != null){
                String[] data = line.split(";");
                
                if(data.length >= 5){
                    String TxnId = data[0];
                    String vendorIDS = data[1];
                    String[] dateParts = data[2].split("-");
                    String year = dateParts[2];
                    String month = dateParts[1];
                    String day = dateParts[0];
                    String time = data[3];
                    String amount = data[4];
                    String type = data[5];
                    
                    if(vendorIDS.equalsIgnoreCase(this.vendorId)){
                        System.out.println("Loaded : " + Arrays.toString(data));
                    
                        model.addRow(new Object[]{vendorId, year, month, day, amount, type});
                    }
                                       
                }else{
                    System.out.println("Skipping malformed line: " + line);
                }
            }
            model.fireTableDataChanged();
            
        }catch(IOException e){
            JOptionPane.showMessageDialog(this, "Error loading txn file." + e.getMessage(), "File Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void calculateTotalRevenue(){
        DefaultTableModel model = (DefaultTableModel) RevenueTable.getModel();
        double totalRevenue = 0.0;
        
        for(int i = 0; i < model.getRowCount(); i++){
            String type = (String) model.getValueAt(i, 5);
            
            if(type.equalsIgnoreCase("Credit")){
                String amountStr = (String) model.getValueAt(i, 4);
                
                try{
                    double amount = Double.parseDouble(amountStr);
                    totalRevenue += amount;
                }catch(NumberFormatException e){
                    System.out.println("Invalid amount: " + amountStr);
                }
            }   
        }
        
        RevenueLabel.setText("RM " + String.format("%.2f", totalRevenue));
        
    }
    
