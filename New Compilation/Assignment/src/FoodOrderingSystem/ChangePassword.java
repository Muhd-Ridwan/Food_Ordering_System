    private void ChangeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChangeBtnActionPerformed
        if(String.valueOf(PasswordChange.getPassword()).trim().equalsIgnoreCase(ConfirmPass.getText().trim())){
            if(changePassword(ConfirmPass.getText().trim())){
                JOptionPane.showMessageDialog(this, "Successfully change password.", "System", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Error changing your password", "System", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Your password didn't match !", "System", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ChangeBtnActionPerformed

