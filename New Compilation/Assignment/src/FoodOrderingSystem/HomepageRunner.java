    private void checkRunnerTask(String runnerID) {
        String filename = "Order Status.txt"; // Ensure this file exists
        boolean hasPendingTask = false;

        try (BufferedReader br = new BufferedReader(new FileReader(filename))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] data = line.split(";");

                // Ensure valid row format and check if the runner ID matches
                if (data.length >= 12 && data[2] != null && data[2].equalsIgnoreCase(runnerID)) {
                    String status = data[11]; // Status column index

                    // ✅ If status is NOT "Completed", notify the runner
                    if (!status.equalsIgnoreCase("Completed")) {
                        hasPendingTask = true;
                        break; // Exit loop early once an incomplete task is found
                    }
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error loading file: " + e.getMessage(), "File Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // ✅ Show appropriate message based on task status
        if (hasPendingTask) {
            JOptionPane.showMessageDialog(null, "You have a pending task!", "Task Notification", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Currently, you don't have any pending tasks.", "Task Notification", JOptionPane.INFORMATION_MESSAGE);
        }
    }

