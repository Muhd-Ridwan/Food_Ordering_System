    private void readUserTxt(){
        try(BufferedReader br = new BufferedReader(new FileReader("User.txt"))){
            String read;
            boolean userFound = false;
            
            while((read = br.readLine()) != null){
                String[] data = read.split(";");
                
                String tempUsername = data[2].toLowerCase();
                String tempUserId = data[1];
                String Password = data[3];
                String role = data[4];
                
                if(UsernameTxtBox.getText().equalsIgnoreCase(tempUsername) && new String(PasswordTxtField.getPassword()).equals(Password)){
                    
                    this.userId = tempUserId;
                    userFound = true;
                    
                    switch(role.toLowerCase()){
                        case "customer":
                            CustomerDashboard CD = new CustomerDashboard(this.userId);
                            CD.setVisible(true);
                            break;
                        case "vendor":
                            VendorHome VH = new VendorHome(this.userId);
                            VH.setVisible(true);
                            System.out.println("Vendor");
                            break;
                        case "runner":
                            HomepageRunner HR = new HomepageRunner(this.userId);
                            HR.setVisible(true);
                            break;
                        case "admin":
                            break;
                        case "manager":
                            break;
                        default:
                            JOptionPane.showMessageDialog(this, "Unknown role !", "System", JOptionPane.ERROR_MESSAGE);
                            break;
                    }
                    this.setVisible(false);
                    System.out.println(tempUsername + " " + Password);
                    break;
                }
            }
            
            if(!userFound){
                    JOptionPane.showMessageDialog(this, "Wrong Username or Password.", "System", JOptionPane.INFORMATION_MESSAGE);
                    UsernameTxtBox.setText("");
                    PasswordTxtField.setText("");
                }
        }catch(IOException e){
            System.out.println("Error in reading user txt");
        }
    }

