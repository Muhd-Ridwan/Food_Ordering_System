    private void ReqBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReqBtnActionPerformed
        String inputText = AmountTxtBox.getText().trim();
        
        if(inputText.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter an amount.", "System", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int value = Integer.parseInt(inputText);
        if(value < 20) {
            JOptionPane.showMessageDialog(this, "Amount must be at least 20.", "System", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        this.amount = value;
        writeToNotiTxt();
        JOptionPane.showMessageDialog(this, "Top-up request submitted succesfully!.", "System", JOptionPane.INFORMATION_MESSAGE);
      
    }//GEN-LAST:event_ReqBtnActionPerformed

