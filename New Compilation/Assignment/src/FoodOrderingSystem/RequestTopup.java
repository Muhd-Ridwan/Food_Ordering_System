    private void setAmountValidation(){
        AmountTxtBox.addKeyListener(new KeyAdapter(){
            public void keyTyped(KeyEvent e){
                char c = e.getKeyChar();
                if (!Character.isDigit(c)){
                    e.consume(); // PREVENT INPUT OF TXT BOX NOT DIGIT
                }
            }
        });
    }

    public void writeToNotiTxt(){
        try{
            String read;
            int rowCount = 0;
            
            FileReader fr = new FileReader("Notification.txt");
            BufferedReader br = new BufferedReader(fr);
            
            while((read = br.readLine()) != null){
                rowCount++;
            }
            br.close();
            
            this.notificationId = "N" + String.format("%03d", rowCount + 1);
            
            FileWriter fw = new FileWriter("Notification.txt", true);
            BufferedWriter bw = new BufferedWriter(fw);
            PrintWriter pw = new PrintWriter(bw);
            pw.write(
                    this.notificationId + ";" +
                    this.cid + ";" +
                    "Topup Request" + ";" +
                    formattedDate + "\n"
            );
            pw.close();
        }catch(IOException e){
            JOptionPane.showMessageDialog(this, "There's an error while requesting a topup. Please try again later.", "System", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
